public class ComparsionOperator {
    public static void main(String[] args) {
        int a = 5;
        int b = 6;
        System.out.println(a = b);
        System.out.println(a == b);
        System.out.println(a != b);
        System.out.println(a >= b);
        System.out.println(a <= b);
        System.out.println(a < b);
        System.out.println(a > b);
    }
}
